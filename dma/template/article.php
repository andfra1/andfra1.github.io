        <p>
            Ad.4 Wizyty kontrolne
            Żaden aparat ortodontyczny nie przeprowadza leczenia samodzielnie. Dlatego wymagane jest odbywanie
            systematycznych wizyt kontrolnych, w czasie których ortodonta w odpowiedni sposób reguluje aparat, w celu
            osiągnięcia zamierzonego rezultatu. Tak naprawdę, aparat ortodontyczny jest jedynie pomocniczym narzędziem w
            rękach lekarza.
            Systematyczne odbywanie wizyt kontrolnych jest podstawowym warunkiem prowadzenia leczenia.
            Na tym etapie cały czas badamy czy nowe ustawianie się zębów nie będzie kolidowało z układem mięśniowo –
            stawowym, aby całe leczenie było w zgodzie z teorią funkcjonalną. Nie szukamy wyłącznie kompromisów
            estetycznych. Wzorowa estetyka i wygląd musi być poparta dobrą funkcją zębów, trwałym efektem i brakiem
            skutków ubocznych leczenia.
            Należy pamiętać, iż zaniedbanie systematyczności wizyt kontrolnych znacznie przedłuża leczenie, a w
            niektórych przypadkach stanowi dla ortodonty podstawę do przerwania leczenia.
        </p>
        <p>
            Ad.5 Zdejmowanie aparatu stałego
            Aktywna faza leczenia ortodontycznego kończy się zabiegiem zdjęcia aparatu z zębów. Taki zabieg polega na
            odklejeniu zamków, rurek lub pierścieni, co trwa od kilkudziesięciu sekund do kilku minut, po czym następuje
            zabieg oczyszczenia zębów z pozostałości kleju. Zdejmowanie zamków odbywa się w sposób mechaniczny: za
            pomocą specjalnych kleszczy uciska się zamek, który łatwo odskakuje od zęba.
        </p>
